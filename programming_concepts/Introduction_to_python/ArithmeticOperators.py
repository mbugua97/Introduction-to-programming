# Arithmetic operators are used to perform mathematical operations. 

# 1. Addition (+)
# Syntax: a + b
# Example:

a = 7
b = 4
result = a + b  # 11
print(result)

# 2. Subtraction (-)
# Syntax: a - b
# Example:

a = 7
b = 4
result = a - b  # 3
print(result)

# 3. Multiplication (*)
# Syntax: a * b
# Example:

a = 7
b = 4
result = a * b  # 28
print(result)

# 4. Division (/)
# Syntax: a / b
# Note: Division returns a float result, even if both operands are integers.
# Example:

a = 7
b = 4
result = a / b  # 1.75
print(result)

# 5. Floor Division (//)
# Syntax: a // b
# Description: Returns the integer quotient of division (without the remainder).
# Example:

a = 7
b = 4
result = a // b  # 1
print(result)

# 6. Modulus (%)
# Syntax: a % b
# Description: Returns the remainder of the division.
# Example:

a = 7
b = 4
result = a % b  # 3
print(result)

# 7. Exponentiation (**)
# Syntax: a ** b
# Description: Raises a to the power of b.
# Example:

a = 7
b = 4
result = a ** b  # 2401
print(result)

# Comparison Operators
# Comparison operators compare two values and return a Boolean (True or False).

# Equal to (==)

a = 1
if a == 2:
    print("Number is equal to 2")

# Not equal to (!=)


a = 1
if a != 2:
    print("Number is not equal to 2")
