
# 1. Basic Assignment (=)
# Syntax: a = b
# Description: Assigns the value of b to a.
# Example:


a = 10
print(a)  # 10

# 2. Add and Assign (+=)
# Syntax: a += b
# Description: Adds b to a and assigns the result to a.
# Example:

a = 10
a += 5  # a = a + 5
# print(a)  # 15
# 3. Subtract and Assign (-=)
# Syntax: a -= b
# Description: Subtracts b from a and assigns the result to a.
# Example:

a = 10
a -= 3  # a = a - 3
print(a)  # 7

# 4. Multiply and Assign (*=)
# Syntax: a *= b
# Description: Multiplies a by b and assigns the result to a.
# Example:

a = 10
a *= 2  # a = a * 2
print(a)  # 20

# 5. Divide and Assign (/=)
# Syntax: a /= b
# Description: Divides a by b and assigns the result to a. The result is always a float.
# Example:

a = 10
a /= 2  # a = a / 2
print(a)  # 5.0

# 6. Floor Divide and Assign (//=)
# Syntax: a //= b
# Description: Performs floor division of a by b and assigns the result to a.
# Example:

a = 10
a //= 3  # a = a // 3
print(a)  # 3

# 7. Modulus and Assign (%=)
# Syntax: a %= b
# Description: Calculates the modulus of a by b and assigns the result to a.
# Example:


a = 10
a %= 3  # a = a % 3
print(a)  # 1

# 8. Exponent and Assign (**=)
# Syntax: a **= b
# Description: Raises a to the power of b and assigns the result to a.
# Example:

a = 2
a **= 3  # a = a ** 3
print(a)  # 8